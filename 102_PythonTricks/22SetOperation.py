A = {0, 2, 5, 6, 8, 7};

B = {1, 2, 3, 4, 5, 9}

# logical Or we can Do common
print("Union :", A | B)

# Logical & we can do intersetion
print("Intersection :", A & B)

# - we can do find Difference between two sets.
print("Difference :", A - B)

# - Logical XOR  can do find Symmetric Difference between two sets.
print("Symmetric difference :", A ^ B)